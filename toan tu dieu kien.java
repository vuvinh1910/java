// condition ? valueIfTrue : valueIfFalse;

public class Main {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        // Sử dụng toán tử điều kiện để tìm giá trị lớn hơn
        int max = (a > b) ? a : b;

        System.out.println("The maximum value is: " + max);
    }
}
