// for (khởi_tạo; điều_kiện; cập_nhật) {
//     // Khối lệnh thực thi
// }
public class ForLoopExample {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.println("Lần lặp thứ: " + i);
        }
    }
}
// while (điều_kiện) {
//     // Khối lệnh thực thi
// }
public class WhileLoopExample {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 5) {
            System.out.println("Lần lặp thứ: " + i);
            i++;
        }
    }
}

